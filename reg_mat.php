            <P ALIGN="JUSTIFY"><B> Art. 2&deg;.- </B> El presente Reglamento establece los dispositivos para que el estudiante no incurra en errores o incompatibilidades, a fin de que su avance curricular transcurra en concordancia con su rendimiento acad&eacute;mico.</P>
            <Center>
              <span class ='tlargebb'><a name ='t_2'> TITULO II <Br>
        DISPOSICIONES GENERALES </a></span>
            </Center>
            <P ALIGN="JUSTIFY"><B> Art. 3&deg;.- </B> Se considera estudiante de la Universidad, al que ingresa por cualquier modalidad y permanece en la misma, registrando su matr&iacute;cula de conformidad con el presente Reglamento.</P>
            <P ALIGN="JUSTIFY"><B> Art. 4&deg;.- </B> La matr&iacute;cula es un acto formal y voluntario que acredita la condici&oacute;n de estudiante universitario implicando su compromiso de cumplir la Ley Universitaria, el Estatuto y dem&aacute;s reglamentos de la Universidad Nacional del Altiplano, mediante la observancia de un elevado nivel de comportamiento, dedicaci&oacute;n a los estudios y actividades propias de la Universidad.</P>
            <P ALIGN="JUSTIFY"><B> Art. 5&deg;.- </B> La matr&iacute;cula regular en cada semestre acad&eacute;mico se realizar&aacute; en fecha se&ntilde;alada por el Consejo Universitario previa publicaci&oacute;n, debiendo ser ratificada durante la primera quincena luego de iniciado oficialmente el dictado de clases. De no existir modificaci&oacute;n de parte del interesado la ratificaci&oacute;n ser&aacute; de oficio.</P>
            <P ALIGN="JUSTIFY"><B> Art. 6&deg;.- </B> Podr&aacute; existir matr&iacute;culas opcionales en caso de programarse cursos vacacionales.</P>
            <P ALIGN="JUSTIFY"><B> Art. 7&deg;.- </B> Los costos por derecho de matr&iacute;cula en los cr&eacute;ditos correspondientes ser&aacute;n acordados por el Consejo Universitario.</P>
            <P ALIGN="JUSTIFY"><B> Art. 8&deg;.- </B> El estudiante podr&aacute; matricularse en una Carrera Profesional y excepcionalmente en dos Carreras Profesionales siempre que haya obtenido el derecho de ingreso, cumpla con los requisitos correspondientes y pague las dos tasas establecidas.</P>
            <P ALIGN="JUSTIFY"><B> Art. 9&deg;.- </B> El Vicerrectorado Acad&eacute;mico, a trav&eacute;s de sus instancias implementar&aacute; y supervisar&aacute; el proceso de matr&iacute;culas. La ejecuci&oacute;n es responsabilidad del Director de la Carrera Profesional con el apoyo de la Coordinaci&oacute;n de Registro y Archivo Acad&eacute;mico de cada Facultad.</P>
