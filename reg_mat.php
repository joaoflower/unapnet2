            <P ALIGN="JUSTIFY"><B> Art. 15&deg;.- </B> El promedio ponderado acad&eacute;mico se calcular&aacute; en base a la sumatoria de las notas obtenidas por el estudiante en todos los cursos matriculados en el semestre, cada uno multiplicado por el n&uacute;mero de cr&eacute;ditos asignados a cada curso. La sumatoria dividida entre la suma de los cr&eacute;ditos matriculados, constituye el PP-semestral.</P>
            <P ALIGN="JUSTIFY"><B> Art. 16&deg;.- </B> Los estudiantes pueden matricularse en un m&aacute;ximo de 24 cr&eacute;ditos, salvo en los siguientes casos:</P>
            <Ol>
              <P ALIGN="JUSTIFY">
              <Li> Los estudiantes que hayan obtenido un promedio ponderado/semestre (PP) de &gt; 12 y &lt;= 13, pueden inscribirse en un m&aacute;ximo de 26 cr&eacute;ditos.</LI>
