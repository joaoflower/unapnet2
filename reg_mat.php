        <p></P>
            </Ol>
            <P ALIGN="JUSTIFY"><B> Art. 17&deg;.- </B> Los estudiantes regulares deben matricularse en las asignaturas cumpliendo con los prerrequisitos exigidos, caso contrario ser&aacute;n depurados durante el proceso de ratificaci&oacute;n.</P>
            <P ALIGN="JUSTIFY"><B> Art. 18&deg;.- </B> Los estudiantes invictos (primera matr&iacute;cula), as&iacute; como los que han sido desaprobados por primera vez en una o m&aacute;s asignaturas (segunda matr&iacute;cula), s&oacute;lo pueden matricularse en el creditaje m&aacute;ximo estipulado por la Carrera Profesional por semestre.</P>
            <P ALIGN="JUSTIFY"><B> Art. 19&deg;.- </B> Los requisitos de matr&iacute;cula para los estudiantes regulares son:</P>
            <Ol Type = "a">
              <P ALIGN="JUSTIFY">
              <Li> Constancia de no adeudar a la Universidad por ning&uacute;n concepto (verificada por la Comisi&oacute;n de Matr&iacute;cula).</LI>
              <Li> Recibo de pago por concepto de cr&eacute;ditos matriculados en el semestre acad&eacute;mico.</LI>
              <p></P>
            </Ol>
            <Center>
              <B><a name ='c_33'> CAPITULO III <Br>
        DE LOS ESTUDIANTES CON TERCERA MATR&Iacute;CULA </a></B>
            </Center>
            <P ALIGN="JUSTIFY"><B> Art. 20&deg;.- </B> Los estudiantes con tercera matr&iacute;cula son aquellos que desaprueban una o m&aacute;s asignaturas por segunda vez, debiendo matricularse en ellas obligatoriamente, observando lo siguiente:</P>
            <Ol Type = "a">
              <P ALIGN="JUSTIFY">
              <Li> Si las asignaturas desaprobadas suman un creditaje superior al 50% del permitido semestralmente; s&oacute;lo se matriculan en dichas asignaturas.</LI>
              <Li> Si las asignaturas desaprobadas suman igual o menos del 50%, se matriculan en &eacute;stas, pudiendo adem&aacute;s completar los cr&eacute;ditos restantes con otros cursos.</LI>
              <p></P>
            </Ol>
            <P ALIGN="JUSTIFY"><B> Art. 21&deg;.- </B> Los estudiantes que durante dos semestres consecutivos hubieran obtenido un promedio ponderado <FONT FACE="Symbol">&#163;</FONT> 6 puntos por semestre, ser&aacute;n considerados como estudiantes no regulares y se inscribir&aacute;n en un m&aacute;ximo de 12 cr&eacute;ditos pasando a un per&iacute;odo de observaci&oacute;n durante el semestre en curso.</P>
            <P ALIGN="JUSTIFY"><B> Art. 22&deg;.- </B> La Comisi&oacute;n de Matr&iacute;culas registrar&aacute; la palabra OBSERVADO en el Historial Acad&eacute;mico del estudiante y consignar&aacute; en la Ficha de Matr&iacute;cula la informaci&oacute;n de las asignaturas cursadas en el semestre anterior con sus respectivas notas y cr&eacute;ditos y los cursos en que se matricula durante el semestre en observaci&oacute;n.</P>
            <P ALIGN="JUSTIFY"><B> Art. 23&deg;.- </B> La condici&oacute;n de OBSERVADO ser&aacute; levantada si el estudiante aprueba la asignatura (s), pudiendo continuar sus estudios como alumno regular; y si desaprueba nuevamente la signatura (s), ser&aacute; suspendido de la Universidad por un Semestre Acad&eacute;mico.</P>
            <Center>
              <B><a name ='c_34'> CAPITULO IV <br>
        DE LOS ESTUDIANTES SUSPENDIDOS </a></B>
            </Center>
            <P ALIGN="JUSTIFY"><B> Art. 24&deg;.- </B> Los estudiantes suspendidos son aquellos que se matriculan en una asignatura (s) por cuarta vez, luego de haber cumplido la sanci&oacute;n de suspensi&oacute;n por un Semestre Acad&eacute;mico. En este caso a su reincorporaci&oacute;n a la Universidad s&oacute;lo se matriculan en la asignatura (s) desaprobadas.</P>
            <P ALIGN="JUSTIFY"><B> Art. 25&deg;.- </B> La condici&oacute;n de CUARTA MATR&Iacute;CULA es levantada, si el estudiante aprueba la asignatura (s) pudiendo continuar sus estudios como alumno regular. En caso de ser reprobado es separado de la Universidad Nacional del Altiplano en forma definitiva.</P>
            <Center>
              <B><a name ='c_35'> CAPITULO V <Br>
        DE LA MATR&Iacute;CULA POR CAMBIO DE CURR&Iacute;CULO </a></B>
            </Center>
            <P ALIGN="JUSTIFY"><B> Art. 26&deg;.- </B> La matr&iacute;cula por cambio de curr&iacute;culo, comprende a estudiantes que por el grado de avance acad&eacute;mico son afectados por la implementaci&oacute;n de un nuevo Plan de Estudios vigente en la Carrera Profesional.</P>
            <P ALIGN="JUSTIFY"><B> Art. 27&deg;.- </B> La matr&iacute;cula del estudiante afectado por la implementaci&oacute;n de una nueva estructura curricular se resuelve de acuerdo al cuadro de equivalencias y convalidaciones establecido en cada Carrera Profesional.</P>
            <P ALIGN="JUSTIFY"><B> Art. 28&deg;.- </B> Las asignaturas no comprendidas en el cuadro de equivalencias y convalidaciones ser&aacute;n programadas como cursos regulares, de nivelaci&oacute;n o vacacionales y de acuerdo a un n&uacute;mero m&iacute;nimo de 12 estudiantes, pudiendo desarrollarse cursos dirigidos, si la cantidad de estudiantes es menor.</P>
            <Center>
              <B><a name ='c_36'> CAP&Iacute;TULO VI <Br>
        DE LA MATR&Iacute;CULA DE ALUMNOS ESPECIALES </a></B>
            </Center>
            <P ALIGN="JUSTIFY"><B> Art. 29&deg;.- </B> La matr&iacute;cula de alumnos especiales se apertura para egresados de nivel universitario, que desean participar en calidad de asistentes en los cursos que ofrecen las Carreras Profesionales; a efecto de optar un Certificado, autorizado y suscrito por el Decano de la Facultad.</P>
            <P ALIGN="JUSTIFY"><B> Art. 30&deg;.- </B> Los requisitos exigidos para la matr&iacute;cula son:</P>
            <Ol Type = "a">
              <P ALIGN="JUSTIFY">
              <Li> Solicitud dirigida al Decano de la Facultad correspondiente indicando la asignatura que desea desarrollar.</LI>
              <Li> Copia legalizada del Grado Acad&eacute;mico o del T&iacute;tulo Profesional.</LI>
              <Li> Recibo de pago por derecho de ense&ntilde;anza de acuerdo al n&uacute;mero de cr&eacute;ditos, seg&uacute;n tasa especial.</LI>
              <LI>Dos (02) fotograf&iacute;as a color actualizadas tama&ntilde;o carn&eacute;.</LI>
              <p></P>
            </Ol>
            <Center>
              <B><a name ='c_37'> CAP&Iacute;TULO VII <Br>
        DEL PROCEDIMIENTO DE MATR&Iacute;CULA </a></B>
            </Center>
            <P ALIGN="JUSTIFY"><B> Art. 31&deg;.- </B> El estudiante regular, despu&eacute;s del periodo de ratificaci&oacute;n debe estar matriculado en no menos del 50% de cr&eacute;ditos establecidos por la Carrera Profesional por semestre. Esta condici&oacute;n da derecho al Carn&eacute; Universitario. Se except&uacute;an de esta disposici&oacute;n los estudiantes que se inscriben en todos los cr&eacute;ditos que les falta para concluir sus estudios y los casos especiales de estudiantes con matr&iacute;cula observada.</P>
            <P ALIGN="JUSTIFY"><B> Art. 32&deg;.- </B> La Comisi&oacute;n de Matr&iacute;cula de cada Facultad, verifica si las asignaturas propuestas por el estudiante con el visto bueno del docente consejero, tienen aprobados los prerrequisitos o han sido desaprobados por &uacute;nica vez, as&iacute; como las dem&aacute;s exigencias de matr&iacute;cula establecidas en el presente Reglamento.</P>
            <P ALIGN="JUSTIFY"><B> Art. 33&deg;.- </B> La Biblioteca Central, laboratorios, y otras dependencias de la Universidad har&aacute;n alcance a la Comisi&oacute;n de Matr&iacute;cula la relaci&oacute;n de estudiantes deudores, para que &eacute;stos regularicen su condici&oacute;n de aptos en las oficinas respectivas.</P>
            <P ALIGN="JUSTIFY"><B> Art. 34&deg;.- </B> La matr&iacute;cula es personal o por carta poder. En el &uacute;ltimo caso quedar&aacute; pendiente hasta ser ratificada por el mismo estudiante en un per&iacute;odo m&aacute;ximo de 15 d&iacute;as (periodo de ratificaci&oacute;n). </P>
            <P ALIGN="JUSTIFY"><B> Art. 35&deg;.- </B> El registro de matr&iacute;cula ser&aacute; centralizado por la Unidad de Admisi&oacute;n y Registro Acad&eacute;mico de la Universidad. </P>
            <P ALIGN="JUSTIFY"><B> Art. 36&deg;.- </B> Las n&oacute;minas de alumnos matriculados por orden alfab&eacute;tico, semestres y turnos, ser&aacute;n remitidos en diskette a la Unidad de Admisi&oacute;n y Registro Aca d&eacute;mico, debidamente refrendadas por el Decano de la Facultad.</P>
            <Center>
              <B><a name ='c_38'> CAP&Iacute;TULO VIII <br>
        DE LA CANCELACI&Oacute;N, RETIRO, CAMBIO DE ASIGNATURAS, REINCORPORACIONES Y DEPURACIONES </a></B>
            </Center>
            <P ALIGN="JUSTIFY"><B> Art. 37&deg;.- </B> Pierden derecho de matr&iacute;cula:</P>
            <Ol Type = "a">
              <P ALIGN="JUSTIFY">
              <Li> Los ingresantes que no han hecho uso de su matr&iacute;cula en el a&ntilde;o de ingreso, salvo en caso de reserva de matr&iacute;cula por dos (02) semestres acad&eacute;micos consecutivos o tres (03) alternos, previa justificaci&oacute;n.</LI>
              <Li> Los estudiantes que habi&eacute;ndose matriculado y cursado por lo menos un (01) semestre dejaron de estudiar cuatro (04) semestres consecutivos o seis (06) semestres alternados.</LI>
              <p></P>
            </Ol>
            <P ALIGN="JUSTIFY"><B> Art. 38&deg;.- </B> El estudiante puede retirarse dentro de los 15 d&iacute;as despu&eacute;s del inicio oficial de clases, en un m&aacute;ximo de dos cursos inscritos, previa autorizaci&oacute;n de su Consejero y la ratificaci&oacute;n de la Direcci&oacute;n de la Carrera Profesional. Al t&eacute;rmino de este per&iacute;odo todos los cursos en los que est&aacute; inscrito ser&aacute;n considerados para establecer su rendimiento acad&eacute;mico semestral (preferentemente de asignaturas de nivel superior).</P>
            <P ALIGN="JUSTIFY"><B> Art. 39&deg;. </B> Finalizado el proceso de retiro los estudiantes tienen cinco (05) d&iacute;as h&aacute;biles para solicitar el cambio (s) de asignatura (s). No proceder&aacute;n los retiros parciales extempor&aacute;neos por ning&uacute;n motivo.</P>
            <P ALIGN="JUSTIFY"><B> Art. 40&deg;. </B> Todo curso inscrito y no retirado formalmente dentro del plazo oficial se considera v&aacute;lido para todos los efectos del promedio ponderado semestral incluyendo el calificativo (0.0), si el estudiante no hubiera rendido ninguna prueba de evaluaci&oacute;n.</P>
            <P ALIGN="JUSTIFY"><B> Art. 41&deg;. </B> El retiro total del semestre s&oacute;lo procede por causas de fuerza mayor y caso fortuito que impidan al estudiante continuar sus estudios, cuya solicitud ser&aacute; resuelta por el Consejo de Facultad; asimismo los alumnos que est&eacute;n comprendidos en el marco de la Ley de Servicio Militar, y otros estipulados por ley.</P>
            <P ALIGN="JUSTIFY"><B> Art. 42&deg;.- </B> Los estudiantes que han dejado de estudiar uno o m&aacute;s semestres acad&eacute;micos, mediante el tr&aacute;mite de retiro total, pueden solicitar su reincorporaci&oacute;n a la Facultad. La solicitud procede solamente hasta una (01) semana antes de la fecha se&ntilde;alada para el inicio de la matr&iacute;cula.</P>
            <P ALIGN="JUSTIFY"><B> Art. 43&deg;.- </B> Si la matr&iacute;cula del estudiante excede en uno o m&aacute;s cr&eacute;ditos de lo establecido, se proceder&aacute; a la anulaci&oacute;n de &eacute;stos en el orden siguiente: cursos electivos, luego los cursos obligatorios.</P>
            <P ALIGN="JUSTIFY"><B> Art. 44&deg;.- </B> La matricula en una asignatura sin cumplir el pre-requisito exigido, es causal de anulaci&oacute;n.</P>
            <Center>
              <span class ='tlargebb'><a name ='t_4'> TITULO IV <br>
        DISPOSICIONES COMPLEMENTARIAS </a></span>
            </Center>
            <P ALIGN="JUSTIFY"><B> Art. 45&deg;.- </B> Para evitar cruces de horario y otros inconvenientes, los estudiantes coordinar&aacute;n su matr&iacute;cula con el profesor consejero.</P>
            <P ALIGN="JUSTIFY"><B> Art. 46&deg;.- </B> Los estudiantes deben inscribirse en los cursos obligatorios desaprobados de los semestres inferiores, teniendo en consideraci&oacute;n el criterio de nivelaci&oacute;n.</P>
            <P ALIGN="JUSTIFY"><B> Art. 47&deg;.- </B> Los estudiantes s&oacute;lo ser&aacute;n evaluados en la secci&oacute;n en que se matricula. La transferencia de nota de una secci&oacute;n a otra es improcedente.</P>
            <P ALIGN="JUSTIFY"><B> Art. 48&deg;.- </B> Se cancelan las asignaturas cuyo n&uacute;mero de semestres inferiores matriculados es menor a doce (12). Los estudiantes afectados por el presente art&iacute;culo, tendr&aacute;n la opci&oacute;n de elegir otra asignatura con vacantes disponibles, durante el per&iacute;odo de cambio. Est&aacute;n exceptuados los estudiantes que concluyen sus estudios de formaci&oacute;n profesional en el per&iacute;odo vigente, en cuyo caso el curso toma la modalidad de Dirigido.</P>
            <P ALIGN="JUSTIFY"><B> Art. 49&deg;.- </B> En cada Carrera Profesional se conforma una Comisi&oacute;n de Matr&iacute;cula, acreditada por el Consejo de Facultad mediante Resoluci&oacute;n. Preside la Comisi&oacute;n el Director de Carrera y la conforma el Jefe de la Oficina de Admisi&oacute;n y Registro Acad&eacute;mico de la Facultad y tres (03), profesores de la Carrera Profesional.</P>
            <P ALIGN="JUSTIFY"><B> Art. 50&deg;.- </B> Son funciones de la Comisi&oacute;n de Matr&iacute;cula:</P>
            <Ol Type = "a">
              <P ALIGN="JUSTIFY">
              <Li> Coordinar acciones para una adecuada implementaci&oacute;n del proceso de matr&iacute;cula con las dependencias correspondientes de la Universidad.</LI>
              <Li> Nombrar las Sub-comisiones de Matr&iacute;cula y Consejer&iacute;a que sean necesarias.</LI>
              <Li> Publicar el cronograma de matr&iacute;cula.</LI>
              <Li> Publicar el horario del desarrollo de asignaturas y el creditaje correspondiente.</LI>
              <Li> Publicar la n&oacute;mina de estudiantes matriculados por niveles acad&eacute;micos.</LI>
              <Li> Elaborar el listado de estudiantes matriculados por curso.</LI>
              <Li> Resolver cualquier caso no previsto en el presente Reglamento, referido al proceso de matr&iacute;cula.</LI>
              <Li> Informar los resultados al Consejo de Facultad del proceso de matr&iacute;cula.</LI>
            </Ol>
            <Center>
              <span class ='tlargebb'><a name ='t_5'> TITULO V <Br>
        DISPOSICIONES TRANSITORIAS </a></span>
            </Center>
            <P ALIGN="JUSTIFY"><B> Art. 51&deg;.- </B> El presente Reglamento es aplicable a partir del a&ntilde;o acad&eacute;mico 2000, de conformidad al art. 326&deg; del Estatuto de la Universidad, que norma la semestralizaci&oacute;n en forma obligatoria del Plan de Estudios como primer paso de adecuaci&oacute;n de las Carreras Profesionales de la UNA-Puno, al sistema acad&eacute;mico de Curr&iacute;culo Flexible Semestral por Cr&eacute;ditos.</P>
            <Center>
              <span class ='tlargebb'><a name ='t_6'> TITULO VI <Br>
        DISPOSICIONES FINALES </a></span>
            </Center>
            <P Align = "Justify"><B> Art. 52&deg;.- </B> Compete a las Facultades y a los organismos acad&eacute;micos de la Universidad, cumplir y hacer cumplir el presente Reglamento bajo responsabilidad.</P>
